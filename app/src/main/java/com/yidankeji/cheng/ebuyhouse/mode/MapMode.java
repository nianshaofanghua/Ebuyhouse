package com.yidankeji.cheng.ebuyhouse.mode;

import java.util.ArrayList;

/**
 * 地图的mode
 */

public class MapMode {

    private String _id;
    private String city_id;
    private String id;
    private String type;
    private double lat;
    private double lon;
    private String k;
    private String v;

    public String getK() {
        return k;
    }

    public void setK(String k) {
        this.k = k;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
        this.v = v;
    }

    private String latitude;
    private String longitude;

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    private ArrayList<MapMode> mapModeArrayList;

    public ArrayList<MapMode> getMapModeArrayList() {
        return mapModeArrayList;
    }

    public void setMapModeArrayList(ArrayList<MapMode> mapModeArrayList) {
        this.mapModeArrayList = mapModeArrayList;
    }

    public String get_id() {
        return _id;
    }

    public void set_id(String _id) {
        this._id = _id;
    }

    public String getCity_id() {
        return city_id;
    }

    public void setCity_id(String city_id) {
        this.city_id = city_id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }
}
